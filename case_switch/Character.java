class Character
{
	public static void main(String[] args)
	{
		switch('A')
		{
			case 'D' : System.out.println("I'm D");
					break;
			case 'C' : System.out.println("I'm C");
					break;
			case 'B' : System.out.println("I'm B");
					break;
			case 'A' : System.out.println("I'm A");
					break;
			default : System.out.println("Invalid");
		}
	}
}