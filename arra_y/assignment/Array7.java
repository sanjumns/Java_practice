class Array7 
{
	public static void main(String[] args) 
	{
		char a[]={'A','B','C','D'};
		String b[]={"Apple","Ball","Cat","Dog"};
		System.out.println("Array length is "+a.length+" and "+b.length);
		for (int i=0;i<a.length ;i++ )
		{
			System.out.println(a[i]+" for "+b[i]);
		}
		System.out.println("**********************");
	}
}
